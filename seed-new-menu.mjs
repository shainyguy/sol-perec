async function main() {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    // Delete all food items (keep bar items untouched)
    const del = await client.query(`DELETE FROM menu_items WHERE is_bar = false`);
    console.log(`🗑️  Deleted ${del.rowCount} food items`);

    // Insert new items
    let inserted = 0;
    for (const [idx, item] of ITEMS.entries()) {
      const sortOrder = CATEGORIES.indexOf(item.cat) * 100 + (idx % 100);
      await client.query(
        `INSERT INTO menu_items (name, description, price, category, is_active, is_bar, is_special, is_gluten_free, is_lactose_free, sort_order)
         VALUES ($1, $2, $3, $4, true, false, false, false, false, $5)`,
        [item.name, item.desc || null, item.price, item.cat, sortOrder]
      );
      inserted++;
    }

    await client.query('COMMIT');
    console.log(`✅ Inserted ${inserted} food items across ${CATEGORIES.length} categories`);
  } catch (e) {
    await client.query('ROLLBACK');
    console.error('❌ Error:', e.message);
    process.exit(1);
  } finally {
    client.release();
    await pool.end();
  }
}

main();
